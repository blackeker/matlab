% Trigonometrik Fonksiyonlar (Radyan cinsinden çalýþýr)
sin(pi)     % Sinüs (Sonuç 0'a çok yakýn) -> Örnek: sin(pi/2) ekrana 1 basar
cos(pi)     % Kosinüs (Sonuç: -1) -> Örnek: cos(pi) ekrana -1 basar
tan(pi)     % Tanjant (Sonuç: 0'a çok yakýn) -> Örnek: tan(pi/4) ekrana 1 basar

% Ters Trigonometrik Fonksiyonlar (Sonucu radyan verir)
asin(0)     % Arksinüs (arcsin) -> Örnek: asin(1) ekrana 1.5708 (pi/2) basar
acos(0)     % Arkkosinüs (arccos) -> Örnek: acos(1) ekrana 0 basar
atan(0)     % Arktanjant (arctan) -> Örnek: atan(1) ekrana 0.7854 (pi/4) basar

% Logaritma ve Üstel Fonksiyonlar
exp(2)      % Eksponensiyel (e^2) -> Örnek: exp(1) ekrana 2.7183 (e sayýsý) basar
log(10)     % Doðal logaritma (ln) -> Örnek: log(exp(1)) ekrana 1 basar
log10(10)   % 10 tabanlý logaritma -> Örnek: log10(100) ekrana 2 basar

% Diðer Temel Fonksiyonlar
sqrt(25)    % Karekök (Square Root) -> Örnek: sqrt(16) ekrana 4 basar
abs(3)      % Mutlak deðer (Absolute Value) -> Örnek: abs(-5) ekrana 5 basar